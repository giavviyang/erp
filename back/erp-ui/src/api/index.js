import request from '@/utils/request'

//首页
export function getData() {
  return request({
    url: '/index/data',
    method: 'GET'
  })
}

export function getOrder(type) {
  return request({
    url: `/index/order/${type}`,
    method: 'GET'
  })
}

export function getProduct(type) {
  return request({
    url: `/index/product/${type}`,
    method: 'GET'
  })
}
